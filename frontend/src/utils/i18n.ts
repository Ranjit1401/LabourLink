// Works with any string — no type errors, no crashes
export function t(lang: Language, key: string, replacements?: Record<string, string | number>): string {
  const text = translations[lang]?.[key] ?? translations["en"][key] ?? key;
  if (!replacements) return text;
  return Object.entries(replacements).reduce(
    (acc, [placeholder, value]) => acc.replace(new RegExp(`\\{${placeholder}\\}`, 'g'), String(value)),
    text
  );
}